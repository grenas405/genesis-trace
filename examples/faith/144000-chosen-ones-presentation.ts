presentation.section("Unwavering Loyalty to Catholicism");
presentation.text(`
This is the crucial distinguishing mark:

The 144,000 remain LOYALLY COMMITTED to the Catholic Church.

Not merely cultural Catholics.
Not cafeteria Catholics who pick and choose.
Not Catholics in name only.

But souls who:
  ✓ Submit to the Magisterium's teaching authority
  ✓ Embrace the fullness of Catholic doctrine
  ✓ Participate in the sacramental life
  ✓ Honor the Pope and bishops in communion with Rome
  ✓ Live according to Catholic moral teaching
  ✓ Defend the faith when necessary

EVEN WHILE serving all humanity, they remain rooted in Catholic truth.
EVEN WHILE their gifts benefit everyone, they stand firm in the Church.
EVEN WHILE they experience suffering, they trust the Church's wisdom.
`);

