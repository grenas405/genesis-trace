// Addressing Questions
presentation.chapter("Common Questions Addressed");

presentation.section("Q: Does this create spiritual elitism?");
presentation.text(`
A: No. Here's why:

  1. DIGNITY IS EQUAL:
     All baptized souls have equal dignity before God.
     A simple peasant who loves God is as precious as any chosen one.

  2. DIFFERENT MISSIONS, NOT DIFFERENT WORTH:
     The 144,000 have a specific mission, not superior worth.
     Like apostles vs disciples - different roles, equal value.

  3. SUFFERING IS NOT A CONTEST:
     We don't measure or compete in suffering.
     But we DO recognize that some suffer more than others - this is reality.

  4. GIFTS ARE FOR SERVICE:
     Extraordinary gifts mean extraordinary responsibility.
     "To whom much is given, much is expected" (Luke 12:48).

  5. ALL VOCATIONS ARE HOLY:
     The married person living faithfully is as holy as any chosen one.
     Holiness is measured by love, not by category of calling.
`);

presentation.divider();

presentation.section("Q: How do the Chosen Ones know they are chosen?");
presentation.text(`
A: Discernment through several signs:

  1. PATTERN OF SUFFERING:
     A life marked by extraordinary, prolonged trials
     Suffering that seems disproportionate yet purposeful

  2. RECOGNITION OF GIFTS:
     Awareness of exceptional abilities
     Others recognize and affirm these gifts
     Gifts that truly benefit many people

  3. UNWAVERING LOYALTY DESPITE TRIALS:
     Choosing Catholic faith even when it would be easier to leave
     Maintaining fidelity through darkness
     Deep love for the Church despite suffering

  4. INTERIOR CALLING:
     Sense of specific mission
     Holy Spirit's movement within
     Spiritual direction confirms

  5. FRUITS:
     Their work produces lasting good
     Their service transforms lives
     Their loyalty inspires others

But ultimately: HUMILITY is required.
A true chosen one never boasts, only serves.
`);

