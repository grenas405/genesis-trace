// Introduction
presentation.chapter("Introduction: Understanding Divine Calling");

presentation.section("The Nature of Christian Vocation");
presentation.text(`
All baptized Christians are called to holiness and service.
This universal call is the foundation of the Christian life.

Within this universal calling, there are SPECIFIC vocations:
  • Priests - Called to shepherd and serve the sacraments
  • Religious - Called to consecrated life of prayer and service
  • Married - Called to sanctify the world through family
  • Single - Called to serve God in unique ways

Each calling is sacred, each is essential to the Body of Christ.
`);

